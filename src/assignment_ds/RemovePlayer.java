    private void removeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeActionPerformed
        // TODO add your handling code here:
        String n = name.getText();
    
     if (n.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill in the required field.", "Input Required", JOptionPane.WARNING_MESSAGE);
        return; 
    }

    try {
        // Validate if the player can be removed
        String validationMessage = teamValidator.canRemovePlayer(n);

        // Show the validation message in a JOptionPane attached to the JFrame
        JOptionPane.showMessageDialog(this, validationMessage, "Team Validation", JOptionPane.INFORMATION_MESSAGE);

        // Proceed with removing the player only if validation passes
        if (!validationMessage.equals("Player removed successfully.")) {
            return; // Exit the method if the validation message is not "Player removed successfully."
        }

        // Player removal logic
        String deleteQuery = "DELETE FROM assignment2 WHERE Name = ?";

        try (Connection conn = dbConnection.getConnection();
             PreparedStatement deleteStmt = conn.prepareStatement(deleteQuery)) {

            deleteStmt.setString(1, n);

            int rowsAffected = deleteStmt.executeUpdate();

            if (rowsAffected > 0) {
                // Player successfully removed, open the TeamRoaster frame
                TeamRoaster rosterFrame = new TeamRoaster(dbConnection);
                rosterFrame.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "Player not found.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    } catch (SQLException e) {
        System.err.println("SQL error occurred.");
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "An error occurred while removing the player.", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_removeActionPerformed

